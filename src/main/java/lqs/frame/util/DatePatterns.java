public class DatePatterns {
	/**
	 * 普通日期格式"yyyy-MM-dd"
	 */
	public final static String POPULAR_DATE = "yyyy-MM-dd";
	/**
	 * 普通日期带小时（12制小时格式）"yyyy-MM-dd hh"
	 */
	public final static String POPULAR_DATE_12HOUR = "yyyy-MM-dd hh";
	/**
	 * 普通日期带小时（24制小时格式）"yyyy-MM-dd hh"
	 */
	public final static String POPULAR_DATE_24HOUR = "yyyy-MM-dd HH";
	/**
	 * 普通日期带时间（12制小时格式）"yyyy-MM-dd hh:mm:ss"
	 */
	public final static String POPULAR_DATE12TIME = "yyyy-MM-dd hh:mm:ss";
	/**
	 * 普通日期带时间（24制小时格式）"yyyy-MM-dd hh:mm:ss"
	 */
	public final static String POPULAR_DATE24TIME = "yyyy-MM-dd HH:mm:ss";
}
